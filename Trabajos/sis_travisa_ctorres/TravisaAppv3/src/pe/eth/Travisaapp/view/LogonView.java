  private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
   
      try {
      // Datos
      String usuario = txtUsuario.getText();
      String clave = String.valueOf(txtClave.getPassword());
      // Proceso
      LogonController control = new LogonController();
        try {
            if (control.validar(usuario, clave)) {
                objTimer = new Timer(30, new ClaseTime());
                objTimer.start();
                jLabel5.setVisible(true);
                jProgressBar1.setVisible(true);
                
//            } else if(!control.validar(usuario, clave)) {
//                JOptionPane.showMessageDialog(null, "Acceso denegado...");
        }
        } catch (Exception e) {
     Logger.getLogger(LogonView.class.getName()).log(Level.SEVERE, null, e);
     Mensaje.showError(rootPane, e.getMessage());
       
        }   
    } catch (Exception e) {
      JOptionPane.showMessageDialog(null, "Acceso denegado...");
    }
  }//GEN-LAST:event_btnIngresarActionPerformed

